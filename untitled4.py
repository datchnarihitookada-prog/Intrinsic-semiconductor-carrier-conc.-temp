overlaps = []

# 計算格子の設定
N = 1000
L_total = 30e-9  # 全体の構造長 [m]
z = np.linspace(0, L_total, N)
dz = z[1] - z[0]

for L_w in well_thicknesses:
    # 初期ポテンシャル設定（障壁部分） [J]
    V_e = np.full(N, 0.5 * e)  # 電子ポテンシャル
    V_h = np.full(N, 0.5 * e)  # 正孔ポテンシャル

    # 井戸領域（中央）
    well_start = L_total / 2 - L_w / 2
    well_end = L_total / 2 + L_w / 2
    in_well = (z >= well_start) & (z <= well_end)

    # 線形ポテンシャル（分極電場の影響） [J]
    V_e[in_well] = -e * F * (z[in_well] - well_start)  # 電子用
    V_h[in_well] = +e * F * (z[in_well] - well_start)  # 正孔用

    # シュレディンガー方程式を解く
    def solve_schrodinger(V, mass):
        kin_const = hbar**2 / (2 * mass * dz**2)
        main_diag = 2 * kin_const + V
        off_diag = -kin_const * np.ones(N - 1)
        energies, wavefuncs = eigh_tridiagonal(main_diag, off_diag)
        psi = wavefuncs[:, 0]
        norm = np.sqrt(np.sum(np.abs(psi)**2) * dz)
        return energies[0], psi / norm

    E_e, psi_e = solve_schrodinger(V_e, me_eff)
    E_h, psi_h = solve_schrodinger(V_h, mh_eff)

    # 重なり積分 |⟨ψe|ψh⟩|^2
    overlap = np.abs(np.sum(np.conj(psi_e) * psi_h) * dz)**2
    overlaps.append(overlap)


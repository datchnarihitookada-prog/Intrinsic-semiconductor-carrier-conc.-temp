import numpy as np
import matplotlib.pyplot as plt
from scipy.constants import hbar, e, m_e
from scipy.linalg import eigh_tridiagonal

# パラメータ
me_eff = 0.2 * m_e   # 電子の有効質量
mh_eff = 1.0 * m_e   # 正孔の有効質量
F = 1e7              # 分極電場 [V/m]（例として1e7 V/m = 100 kV/cm）
L_b = 5e-9           # 障壁厚 [m]（固定）

# 井戸厚の範囲
well_thicknesses = np.linspace(2e-9, 10e-9, 50)
overlaps = []

# 数値計算用格子
N = 1000
L_total = 30e-9
z = np.linspace(0, L_total, N)
dz = z[1] - z[0]

for L_w in well_thicknesses:
    # ポテンシャルプロファイル（電子と正孔で反対向き）
    V_e = np.zeros_like(z)
    V_h = np.zeros_like(z)

    # InGaN井戸領域（中央部）
    well_start = L_total/2 - L_w/2
    well_end = L_total/2 + L_w/2
    for i in range(N):
        if well_start <= z[i] <= well_end:
            V_e[i] = -e * F * (z[i] - well_start)   # 線形電場（電子）
            V_h[i] = +e * F * (z[i] - well_start)   # 線形電場（正孔）
        else:
            V_e[i] = 0.5   # 高障壁 [eV]
            V_h[i] = 0.5

    # 電子・正孔のシュレディンガー方程式を数値的に解く（1次元）
    def solve_schrodinger(V, mass):
        kin_const = hbar**2 / (2 * mass * dz**2)
        main_diag = 2 * kin_const + V
        off_diag = -kin_const * np.ones(N-1)
        energies, wavefuncs = eigh_tridiagonal(main_diag, off_diag)
        psi = wavefuncs[:, 0] / np.sqrt(np.sum(np.abs(wavefuncs[:, 0])**2) * dz)
        return energies[0], psi

    E_e, psi_e = solve_schrodinger(V_e, me_eff)
    E_h, psi_h = solve_schrodinger(V_h, mh_eff)

    # 重なり積分 |∫ψe * ψh dz|^2
    overlap = np.abs(np.sum(psi_e * psi_h) * dz)**2
    overlaps.append(overlap)


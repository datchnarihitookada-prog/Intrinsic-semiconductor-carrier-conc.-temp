import numpy as np
import matplotlib.pyplot as plt

# 定数（単位系は自然単位系: ℏ = 1, 2m = 1）
hbar = 1
m = 0.5  # 2m=1より
a = 1.0  # 格子定数
P = 10.0  # ポテンシャル強度（無次元）

# エネルギー範囲の設定
E_vals = np.linspace(0.01, 100, 1000)  # エネルギーのスキャン範囲
k_vals = []  # 許されるkの値を格納

for E in E_vals:
    q = np.sqrt(2 * m * E) / hbar
    lhs = np.cos(q * a) + P / (q * a) * np.sin(q * a)
    if np.abs(lhs) <= 1:
        k = np.arccos(lhs) / a
        k_vals.append([E, k])
        # 負のkも加える（対称性）
        k_vals.append([E, -k])

# 配列に変換してプロット
k_vals = np.array(k_vals)
plt.figure(figsize=(8, 6))
plt.scatter(k_vals[:, 1], k_vals[:, 0], s=1, color='blue')
plt.xlabel("Wavevector k")
plt.ylabel("Energy E")
plt.title("Kronig-Penney Model: E-k Dispersion")
plt.grid(True)
plt.show()


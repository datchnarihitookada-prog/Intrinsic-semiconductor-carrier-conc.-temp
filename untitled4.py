from scipy.constants import epsilon_0, e, hbar, m_e

# -------------------------
# 固定パラメータ
# -------------------------
d_AlGaN = 20e-9  # AlGaN厚さ [m]
eps_r = 9.5
m_eff = 0.2 * m_e

# Al組成範囲
x_vals = np.linspace(0.0, 0.5, 100)
n_2DEG_vals = []

# -------------------------
# Al組成ごとの計算ループ
# -------------------------
for x_Al in x_vals:
    # 分極電荷密度 σ_pol(x) [C/m²]
    # 経験式（Ambacher et al. JAP 2000）：界面での分極電荷
    sigma_pol = -0.034 + 0.057 * x_Al  # [C/m²]

    if sigma_pol < 0:
        n_2DEG_vals.append(0)  # 電荷が負なら2DEGは形成されない
        continue

    # AlGaN層の電界 [V/m]
    E_field = sigma_pol / (eps_r * epsilon_0)

    # トライアングルポテンシャルモデルでの基底準位 [J]
    E1 = ((9 * np.pi / 8)**(2/3) * (hbar**2 * e * E_field)**(2/3)) / (2 * m_eff)

    # 2DEG密度 [m⁻²] → [cm⁻²]
    n_2DEG_m2 = (2 * m_eff * E1) / (np.pi * hbar**2)
    n_2DEG_cm2 = n_2DEG_m2 * 1e-4
    n_2DEG_vals.append(n_2DEG_cm2)

# -------------------------
# グラフ描画
# -------------------------
plt.figure(figsize=(8,5))
plt.plot(x_vals, n_2DEG_vals, color='darkgreen')
plt.xlabel('Al composition (x)')
plt.ylabel('2DEG density (cm$^{-2}$)')
plt.title(f'2DEG Density vs Al Composition (AlGaN thickness = {d_AlGaN*1e9:.0f} nm)')
plt.grid(True)
plt.tight_layout()
plt.show()


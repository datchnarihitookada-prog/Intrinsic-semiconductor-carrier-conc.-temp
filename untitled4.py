import numpy as np
import matplotlib.pyplot as plt
from scipy.optimize import root_scalar

# 定数定義（単位はメートル）
cGaN = 5.189e-10
cInN = 5.760e-10
aGaN = 3.185e-10
aInN = 3.584e-10
aAlN = 3.112e-10
cAlN = 4.982e-10
nu_GaN = 0.271
nu_AlN = 0.266

# 各種関数定義
def da(xg):
    return xg * (aGaN - aAlN)

def f(xg):
    return da(xg) / aAlN

def b(xg):
    return aAlN + xg * (aGaN - aAlN)

def nu(xg):
    return nu_GaN - xg * (nu_GaN - nu_AlN)

def F1(hc, xg):
    term1 = b(xg) / (2 * np.pi * f(xg))
    term2 = 1 / (1 + nu(xg))
    term3 = np.log(hc / b(xg)) + 1
    return term1 * term2 * term3 - hc

# 結果格納用リスト
q1_list = []
q2_list = []

# メインループ（xg = 0.01 * n）
for n in range(1, 101):  # 1 〜 100
    xg = 0.01 * n
    try:
        sol = root_scalar(F1, args=(xg,), bracket=[1e-12, 1e-7], method='brentq')
        if sol.converged:
            hc = sol.root
            q1_list.append(1 - xg)
            q2_list.append(hc * 1e9)  # 単位：nm
    except ValueError:
        # 解が見つからない場合
        continue

# データをNumPy配列に変換
A = np.column_stack((q1_list, q2_list))

# プロット（対数スケール）
plt.figure(figsize=(6, 4))
plt.plot(q1_list, q2_list, marker='o')
plt.xlim(0, 1)
plt.yscale('log')
plt.ylim(1, 1000)
plt.xlabel('1 - xg')
plt.ylabel('Critical thickness (nm)')
plt.title('Critical thickness vs. relaxation rate')
plt.grid(True)
plt.tight_layout()
plt.show()

# データの保存
np.savetxt("Thickness.dat", A, fmt="%.6f", header="1 - xg\tThickness [nm]")




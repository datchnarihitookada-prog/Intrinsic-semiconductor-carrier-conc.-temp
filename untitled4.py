import numpy as np
import matplotlib.pyplot as plt
from scipy.optimize import root_scalar

# 定数定義（単位：m）
aGaN = 3.185e-10
aAlN = 3.112e-10
nu_GaN = 0.271
nu_AlN = 0.266

# 関数定義
def da(xg):
    return xg * (aGaN - aAlN)

def f(xg):
    return da(xg) / aAlN

def b(xg):
    return aAlN + xg * (aGaN - aAlN)

def nu(xg):
    return nu_GaN - xg * (nu_GaN - nu_AlN)

def F1(hc, xg):
    try:
        bx = b(xg)
        fx = f(xg)
        nux = nu(xg)
        if hc <= bx or fx == 0:
            return np.nan
        return (bx / (2 * np.pi * fx)) * (1 / (1 + nux)) * (np.log(hc / bx) + 1) - hc
    except Exception:
        return np.nan

# 結果格納用リスト
q1_list = []
q2_list = []

# 探索ループ
for n in range(1, 101):
    xg = 0.01 * n
    bx = b(xg)
    hc_min = bx * 1.01
    hc_max = 1e-6  # 十分広い範囲で探索
    F_min = F1(hc_min, xg)
    F_max = F1(hc_max, xg)

    try:
        if np.isnan(F_min) or np.isnan(F_max):
            raise ValueError("F1 = nan")
        if F_min * F_max > 0:
            raise ValueError("符号が変わらない")

        sol = root_scalar(F1, args=(xg,), bracket=[hc_min, hc_max], method='brentq')
        if sol.converged:
            hc = sol.root
            q1_list.append(1 - xg)
            q2_list.append(hc * 1e9)
            print(f"xg={xg:.2f}, hc={hc * 1e9:.2f} nm")
        else:
            print(f"xg={xg:.2f} → 未収束")
    except Exception as e:
        print(f"xg={xg:.2f} → 解なし: {e}")

        # オプション：F1の挙動を表示（必要な場合だけ）
        hc_vals = np.logspace(np.log10(hc_min), np.log10(hc_max), 200)
        F_vals = [F1(hc, xg) for hc in hc_vals]

        plt.figure(figsize=(5, 3))
        plt.plot(hc_vals, F_vals, label=f'xg={xg:.2f}')
        plt.axhline(0, color='gray', linestyle='--')
        plt.xscale('log')
        plt.xlabel("hc [m]")
